# #statements or two types--->1)normal 2)control statements

# # control statements are used to control the flow of execution of the program
# #1) if statement
#     #syntax: if condition:  
# #            statement(s)
# #example:
num=5
if num > 2:
    print("5 is big")


# #2) if-else statement
# #syntax: if condition:      
# #            statement(s)
# #        else:
# #            statement(s)
num = -5
if num > 0:
    print("5 is positive number")
else:
    print("5 is negative number")

